def _normalize_lookup(value: str) -> str:
    outside: list[str] = []
    depth = 0
    for character in value:
        if character in "（(":
            depth += 1
        elif character in "）)":
            depth = max(0, depth - 1)
        elif depth == 0:
            outside.append(character)
    return "".join("".join(outside).split())


